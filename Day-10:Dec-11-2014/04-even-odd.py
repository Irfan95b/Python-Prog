
#(Program to demo with Multiple-if stmts with Even,Odd,Neither of them)

num = int(input("Enter any integer number : "))

if(num == 0):
    print("Please Enter any number greater than zero");
if(num%2 == 0):
    print("Given number is even");
if(num%2 == 1):
    print("Given number is odd");

print("End of program\n");
