# 1. new line charecter (\n) :
#   Moves the courcer to the next line.

print("Hello\nWelcome\nto\nPython\nProgramming")


print("\n-------------------------------------------------------\n")

# 2. tab space charecter (\t) :
# Moves courcer with the 8 spaces forword or one tab in the same line.

print("Hello\n\tWelcome\nto\n\tPython\nProgramming\t")

print("\n-------------------------------------------------------\n")

# 3. back space charecter (\b):
# Moves courcer to one space back in the same line.


print("Hello Welcome to Python Programming")
print("Hello \bWelcome \bto \bPython \bProgramming")
print("Hello\bWelcome\bto\bPython\bProgramming")

print("\n-------------------------------------------------------\n")

# 4. carrieage or return charecter (\r)
# Takes the courser the begining of the same line.

print("Hello Welcome to Python \rProgramming")


print("\n-------------------------------------------------------\n")

# 5. To use back slash in backword in string printing we use bouble back slach (\\)

print("Hello \\\\ wellcome {data}  // to python programming")

print("\n-------------------------------------------------------\n")

